export const getAddonFromId = (id) => ADDON_PROVIDERS_RAW.find((addon) => addon.id === id);

export const getAddonMetadataFromId = (addonId) => ADDON_PLAN_METADATA.find((metadata) => metadata.id === addonId);

export const getEnhancedPlansFromId = (addonId) => {
  const addon = getAddonFromId(addonId);
  const metadata = getAddonMetadataFromId(addonId);
  if (metadata.categories == null) {
    return Object.fromEntries(addon.plans.map((plan) => [plan.name, [plan]]));
  }

  return Object.fromEntries(
    metadata.categories
      .map(
        (cat) => [cat, addon.plans.filter((plan) => plan.name.startsWith(cat))],
      ),
  );
};
